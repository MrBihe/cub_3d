#include "so_long.h"

void	ft_str(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(2, &str[i], 1);
		i++;
	}
	write(2, "\n", 1);
}

void	ft_error(char *str)
{
	ft_str(str);
	exit(0);
}
